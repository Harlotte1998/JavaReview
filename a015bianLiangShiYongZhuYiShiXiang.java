package heimachengxuyuan;

public class a015bianLiangShiYongZhuYiShiXiang {

	public static void main(String[] args) {
		/*变量使用注意事项
		 * 
		 * 1.0 数据类型
		 *   
		 *   1.1 基本数据类型
		 *     1.1.1 数值型
		 *       整数      byte    short   int    long
		 *      浮点数   float   double
		 *       字符      char
		 *    
		 *     1.1.2 非数值型
		 *       布尔      boolean
		 *       
		 *   1.2 引用数据类型
		 *        类        class
		 *        接口    interface
		 *        数组    [  ]
		 * */
		
		/*
		 * 注意事项
		 * 
		 * 1.   long 类型的变量定义的时候 为了防止整数过大
		 *   后面要加L
		 * 2.   float类型的变量定义的时候，为了防止不兼容
		 *   后面要加F   
		 * 
		 * */
		
		
		
		
		//定义byte类型的变量
		byte a=12;
		System.out.println(a);
		
		
		//定义short类型的变量
		short b=100;
		System.out.println(b);
		
		
		//定义int类型的变量
		int c=45665;
		System.out.println(c);
		
		
		//定义double类型的变量
		double d=45.65;
		System.out.println(d);
		
		
		//定义charr类型的变量
		char e='a';
		//定义boolean类型的变量
		boolean f=true;
		System.out.println(f);
		
		
		//定义long类型的变量
		long g=1000000000000l;
		System.out.println(g);
		
		
		//定义float类型的变量
		float h= 12.65f;
		System.out.println(h);
		
		
		
		
		
		
		
		
		
	}
}
