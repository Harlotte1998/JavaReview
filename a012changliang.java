package heimachengxuyuan;

public class a012changliang {
	public static void main(String[] args) {
		/*
		 * 字符串常量   用双引号括起来的内容   "这是一个常量"
		 * 整数   常量     不带小数的数字
		 * 小数   常量    带小数的数字
		 * 字符   常量    用单括号括起来的内容
		 * 布尔   常量    表示真假  true  false
		 * 空常量     一个特殊的值  空值  null    
		 * 
		 * 
		 * 
		 * 
		 * */
		
		//字符长常量
		System.out.println("hellowold");
		System.out.println("这是一个字符常量");
		System.out.println("----------------------------------------------------");
		//整数常量
		System.out.println(56545);
		System.out.println(-4554);
		System.out.println("----------------------------------------------------");
		
		//小数常量
		System.out.println(56.545);
		System.out.println(-45.54);
		System.out.println("----------------------------------------------------");

		//字符常量   单个字或者单个字母    单个！！！
		System.out.println('A');
		System.out.println('字');		
		System.out.println("----------------------------------------------------");

		//布尔常量
		System.out.println(true);
		System.out.println(false);
		System.out.println("----------------------------------------------------");

		//空常量
		//空常量不能直接输出
		//System.out.println(null);
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
}
